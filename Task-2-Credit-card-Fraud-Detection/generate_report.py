# Colors
PRIMARY_BLUE = HexColor("#1a5276")
ACCENT_BLUE = HexColor("#2980b9")
LIGHT_GRAY = HexColor("#f4f6f7")
DARK_GRAY = HexColor("#2c3e50")

def create_styles():
    styles = getSampleStyleSheet()
    
    # Title style
    styles.add(ParagraphStyle(
        name='ReportTitle',
        parent=styles['Title'],
        fontSize=24,
        textColor=PRIMARY_BLUE,
        spaceAfter=20,
        alignment=TA_CENTER,
        fontName='Helvetica-Bold'
    ))
    
    # Subtitle
    styles.add(ParagraphStyle(
        name='Subtitle',
        parent=styles['Normal'],
        fontSize=12,
        textColor=DARK_GRAY,
        spaceAfter=30,
        alignment=TA_CENTER,
        fontName='Helvetica-Oblique'
    ))
    
    # Section heading
    styles.add(ParagraphStyle(
        name='SectionHeading',
        parent=styles['Heading1'],
        fontSize=16,
        textColor=PRIMARY_BLUE,
        spaceBefore=20,
        spaceAfter=10,
        fontName='Helvetica-Bold',
        borderWidth=0,
        borderPadding=0,
        borderColor=ACCENT_BLUE,
        borderRadius=0,
    ))
    
    # Subsection
    styles.add(ParagraphStyle(
        name='SubHeading',
        parent=styles['Heading2'],
        fontSize=13,
        textColor=ACCENT_BLUE,
        spaceBefore=12,
        spaceAfter=6,
        fontName='Helvetica-Bold'
    ))
    
    # Body text
    styles.add(ParagraphStyle(
        name='ReportBody',
        parent=styles['Normal'],
        fontSize=10,
        leading=14,
        textColor=DARK_GRAY,
        alignment=TA_JUSTIFY,
        spaceAfter=8,
        fontName='Helvetica'
    ))
    
    # Bullet style
    styles.add(ParagraphStyle(
        name='BulletText',
        parent=styles['Normal'],
        fontSize=10,
        leading=13,
        textColor=DARK_GRAY,
        leftIndent=20,
        fontName='Helvetica'
    ))
    
    # Code style
    styles.add(ParagraphStyle(
        name='CodeBlock',
        parent=styles['Code'],
        fontSize=8,
        leading=10,
        backColor=LIGHT_GRAY,
        borderWidth=0.5,
        borderColor=HexColor("#bdc3c7"),
        borderPadding=6,
        fontName='Courier'
    ))
    
    # Footer
    styles.add(ParagraphStyle(
        name='Footer',
        parent=styles['Normal'],
        fontSize=8,
        textColor=HexColor("#7f8c8d"),
        alignment=TA_CENTER
    ))
    
    return styles

def add_header_footer(canvas, doc):
    canvas.saveState()
    
    # Header
    canvas.setFillColor(PRIMARY_BLUE)
    canvas.rect(0, letter[1] - 40, letter[0], 40, fill=True, stroke=False)
    canvas.setFillColor(white)
    canvas.setFont('Helvetica-Bold', 10)
    canvas.drawString(30, letter[1] - 25, "Credit Card Fraud Detection — Final Project Report")
    canvas.drawRightString(letter[0] - 30, letter[1] - 25, "Task 4 | SLASHMARK-kvsa")
    
    # Footer
    canvas.setFillColor(HexColor("#ecf0f1"))
    canvas.rect(0, 0, letter[0], 30, fill=True, stroke=False)
    canvas.setFillColor(DARK_GRAY)
    canvas.setFont('Helvetica', 8)
    canvas.drawString(30, 12, "Generated by Grok | xAI — May 2026")
    canvas.drawRightString(letter[0] - 30, 12, f"Page {doc.page}")
    
    canvas.restoreState()

